
package Sample1;

public class Sample7
{
	public static void main(String[] args) {
		
		
		//  *
		// **
		//***
		
		int Star=1;
		int Space = 2;
		for(int i=1;i<=3;i++)
			
		{
			for(int j=1;j<=Space;j++)
			{
				System.out.print(" ");
				
			}
			for(int j=1;j<=Star;j++)
			{
				System.out.print("*");
				
			}
			System.out.println();
			Star++;
			Space--;
		}
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
